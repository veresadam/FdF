#include "../fdf.h"

int		is_clean(char *s)
{
	while (*s)
	{
		if (!(*s == ' ' || (*s >= '0' && *s <= '9') || (*s == '-' &&
						(*(s + 1) >= '0' && *(s + 1) <= '9'))))
			return (0);
		s++;
	}
	return (1);
}
